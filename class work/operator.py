##6.Identical operators
#----------------------------------------------------------------------------------------------------------------------------------------------------------

a = [1, 2, 3, 4]
b = [1, 2, 3, 4]
print("a is b:",a is b)
c = a
print("a is c:",a is c)
print("id(a):",id(a))
print("id(b):",id(b))
print("id(c):",id(c))

print("a is not b:",a is not b)
print("a is not c:",a is not c)

#output:-
##a is b: False
##a is c: True
##id(a): 2277204277184
##id(b): 2277203534912
##id(c): 2277204277184
